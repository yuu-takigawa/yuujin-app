// ─── ユーティリティ ───
export function pickRandom<T>(arr: T[]): T {
  return arr[Math.floor(Math.random() * arr.length)];
}

export function pickRandomN<T>(arr: T[], n: number): T[] {
  const shuffled = [...arr].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, n);
}

export function randomFullName(gender: string): string {
  const lastName = pickRandom(LAST_NAMES);
  const firstNames = gender === '男性' ? FIRST_NAMES_MALE : FIRST_NAMES_FEMALE;
  return lastName + pickRandom(firstNames);
}

export function randomAge(): number {
  return 18 + Math.floor(Math.random() * 22); // 18-39
}

